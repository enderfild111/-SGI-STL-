#include "CommonConnectionPool.h"
//构造函数和析构函数
CommonConnectionPool::CommonConnectionPool()
{
	if (!loadConfigFile()) {
		return;
	}
	/*
	创建初始数量的连接
	*/
	for (int i = 0; i < _initSize; i++) {
		Connection* p = new Connection();
		p->connect(_ip, _port, _username, _password, _dbname);
		_connectionQue.push(p);
		_connectionCnt++;
	}
	//启动一个新的线程，作为连接的生产者
}

CommonConnectionPool::~CommonConnectionPool()
{
}
//线程安全的懒汉单例模式
CommonConnectionPool* CommonConnectionPool::getConnectionPool() {
	static CommonConnectionPool pool;//静态局部变量，编译器自动lock和unlock，线程安全
	return &pool;
} 
bool CommonConnectionPool::loadConfigFile() {
	FILE* pf = fopen("mysql.ini", "r");
	if (pf == nullptr) {
		LOG("mysql.ini not found");
		return false;
	}
	while (!feof(pf)) {
		char line[1024] = { 0 };
		fgets(line, 1024, pf);
		string str = line;
		int idx = str.find("=",0);
		if (idx == -1) {//空行或者注释行
			continue;
		}
		//解析key-value
		//password = root\n
		int endIdx = str.find("\n", idx);
		string key = str.substr(0, idx);
		string value = str.substr(idx + 1, endIdx - idx - 1);
		if (key == "ip") {
			_ip = value;
		}
		else if (key == "port") {
			_port = atoi(value.c_str());
		}
		else if (key == "dbname") {
			_dbname = value;
		}
		else if (key == "username") {
			_username = value;
		}
		else if (key == "password") {
			_password = value;
		}
		else if (key == "initSize") {
			_initSize = atoi(value.c_str());
		}
		else if (key == "maxSize") {
			_maxSize = atoi(value.c_str());
		}
		else if (key == "maxIdleTime") {
			_maxIdleTime = atoi(value.c_str());
		}
		else if (key == "connectionTimeout") {
			_connectionTimeout = atoi(value.c_str());
		}
	}
	return true;
}